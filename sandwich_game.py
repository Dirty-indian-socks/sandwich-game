def Финал(бутерброд):
    print(f"\n{Colors.RED}👹 ФИНАЛЬНАЯ БИТВА С ПОЖИРАТЕЛЕМ ВКУСОВ!{Colors.END}")
    враг = Враг("Пожиратель Вкусов", 150, 20)
    if бой(бутерброд, враг):
        print(f"\n{Colors.GREEN}🎊 ПОБЕДА! Ты восстановил гармонию вкуса!{Colors.END}")
